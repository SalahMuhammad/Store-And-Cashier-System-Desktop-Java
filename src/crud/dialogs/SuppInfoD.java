    public void dialogInitializer( int id, String name, String phone ) {
        this.id = id;
        nameText.setText( name );
        phoneText.setText( phone );
        
        submitBTN.setText( "تحديث" );
        deleteBTN.setVisible( true );
        
        this.setVisible( true );
    }
    
    void submitAction() {
        int result = -1;
        
        if ( submitBTN.getText() == "اضافة" ) {
            result = new SuppliersInfoC().insert( nameText.getText(), phoneText.getText() );
        } else if ( submitBTN.getText() == "تحديث" ){
            result = new SuppliersInfoC().update( id, nameText.getText(), phoneText.getText() );

            this.dispose();
        }

        if ( result == -1 ) return;

        emptyFields();
    }
    
    private void emptyFields() {
        nameText.requestFocus();
        
        nameText.setText( "" );
        phoneText.setText( "" );
    }
    
